package org.castellum.field;

/**
 * Primitive types are the most basic data types available within the Java language.
 * There are 8: boolean, byte, char, short, int, long, float and double
 */
public enum Field {
    BOOLEAN,  //0
    BYTE,     //1
    CHAR,     //2
    SHORT,    //3
    INTEGER,  //4
    LONG,     //5
    FLOAT,    //6
    DOUBLE,   //7
    STRING    //8
}
